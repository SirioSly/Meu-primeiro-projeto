<h2>Conclusão</h2>
<p>Vimos neste artigo o início da criação de sites em PHP, onde você terá total controle e poderá fazer o que quiser. Quero aproveitar e dizer que estes códigos só foram mostrados para fins de demonstração e não me responsabilizo por seu uso. Você terá que se preocupar com outros aspectos, especialmente questão de segurança e autenticação.</p>
<p>Agora, se você realmente quer criar sites em PHP e você não tem pretensão em ser uma referência de desenvolvimento web PHP ou criar um sistema que leve seu nome e seja comercializado no futuro, enfim, se você não pretende fazer nada tão grandioso, apenas criar um site PHP&nbsp;para uso pessoal ou profissional, recomendo fortemente que utilize um CMS pronto e open-source, como o WordPress.</p>
<p>Estes sistemas já oferecem muitos recursos prontos e com um pouco de codificação extra poderão entregar exatamente o que você espera. O WordPress tem mais 30 mil plugins gratuitos e outros milhares de plugins pagos que atendem a quase toda demanda necessária. Eu, particularmente, preciso muito pouco de desenvolver algo tão personalizado.</p>
