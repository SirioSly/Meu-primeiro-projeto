<h2 itemprop="name" class="entry-title eltdf-post-title">
            Como Criar um Site em PHP    </h2>					
	<div class="eltdf-post-image">
                            <img width="791" height="527" src="https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/Como-Criar-um-Site-PHP.jpg" class="attachment-full size-full wp-post-image" alt="" srcset="https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/Como-Criar-um-Site-PHP.jpg 791w, https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/Como-Criar-um-Site-PHP-300x200.jpg 300w" sizes="(max-width: 791px) 100vw, 791px">                	</div>
				</div>
                <div class="eltdf-post-text-main">
                    <p>Continuando a série de posts que falo sobre a criação de sites, vou mostrar a você neste post Como Criar um Site em PHP.</p>
<p>PHP é, atualmente, a principal linguagem de programação utilizada em aplicações web. Os principais sites e plataformas estão sendo construídos em PHP. Então, se você pretende ter criar um site atual e encontrar bastante suporte&nbsp;(até gratuito) na internet, o PHP é uma excelente escolha.</p>
<p><img class="aligncenter size-full wp-image-2059" src="https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/serverside-language.png" alt="Comparativo das Linguagens de Desenvolvimento Web" width="666" height="278" srcset="https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/serverside-language.png 666w, https://www.m3marketingdigital.com.br/wp-content/uploads/2015/06/serverside-language-300x125.png 300w" sizes="(max-width: 666px) 100vw, 666px"></p>
<p>No entanto, se você quer mesmo criar um site em PHP, você pode ter que decidir entre: Criar um site PHP&nbsp;puro, criar um site PHP&nbsp;utilizando um Framework ou Criar um site PHP&nbsp;usando um CMS open-source. Mas, claro, você pode criar o seu próprio sistema de gerenciamento de conteúdo (CMS), e é isso que vou mostrar a você neste artigo.</p>
<p>Sempre gosto de contar sobre minhas experiências, minha história, acho que isso pode ajudar você a decidir sobre algumas coisas. Assim, gostaria de dizer que, durante algum tempo, eu criei sites usando PHP&nbsp;puro. Depois de um tempo, conforme os projetos foram crescendo e outras necessidades foram surgindo, passei a utilizar Frameworks. Nas duas formas, eu utilizei o PHP para criar meu próprio CMS. Acho que um site dinâmico, sem um gerenciador de conteúdo, não vai ser interessante para você.</p>
<p>Atualmente, eu não utilizo nem o PHP puro e nem Frameworks, utilizo o <a href="http://br.wordpress.org" target="_blank">WordPress</a> – Um excelente CMS open-source, escrito em PHP, que facilita demais a criação de sites. E mesmo que não domine o WordPress, você não vai ter uma curva de aprendizado tão grande. No final do post, vou falar mais sobre o por que eu escolhi o WordPress.</p>
<p>Então, vamos ver <strong>Como Criar um Site em PHP</strong>, melhor, vamos ver como inciar a criação do seu próprio CMS.</p>
<p>Para isto, vamos criar uma <a href="http://php.net/manual/pt_BR/language.oop5.php" target="_blank">Classe PHP</a> para integrar o PHP e o MySQL – que é o principal banco de dados que utilizamos com o PHP – e para mostrar a você o básico de um CMS. Vale dizer que você vai precisar entender alguma coisa de PHP, então para você não se sentir perdido, recomendo alguns recursos e dou o link para baixar os arquivos deste post no final.</p>
<h2>Construindo a Classe PHP</h2>
<p>Então, nosso primeiro passo é criar a classe PHP para o nosso CMS básico. Vamos criar um arquivo chamado de “CMS.php”, colocá-lo em uma pasta chamada “_class” e inserir o código abaixo:</p>
<p>[php]<br>
&lt;?php<br>
class CMS {<br>
  var $host;<br>
  var $usuario;<br>
  var $senha;<br>
  var $bd;<br>
  public function display_public() {}<br>
  public function display_admin() {}<br>
  public function gravar() {}<br>
  public function conectar() {}<br>
  private function criaBD() {}<br>
}<br>
?&gt;<br>
[/php]</p>
<p>No código acima, estamos trabalhando com 4 variáveis e 5 métodos. Estamos utilizando aqui o PHP com <a href="http://www.google.com/url?q=http%3A%2F%2Fpt.wikipedia.org%2Fwiki%2FOrienta%25C3%25A7%25C3%25A3o_a_objetos&amp;sa=D&amp;sntz=1&amp;usg=AFQjCNE8-M-rfLrQRKch3gbhhl_ZolpqPg" target="_blank">orientação a objetos</a>, para facilitar a compreensão do código, deixá-lo mais limpo, e porque acredito que seja a&nbsp;melhor prática&nbsp;para se trabalhar com PHP.</p>
<h2>Variáveis PHP</h2>
<p>As quatro variáveis que estamos utilizando acima são para criarmos a conexão com o banco de dados: <em>$host, $usuario, $senha e $bd</em>. Por enquanto, vamos deixar tudo vazio e partir para a criação do nosso banco de dados através do método “<em>CriaBD()</em>“.</p>
<h2>Criando Tabelas no Banco de Dados</h2>
<p>Vamos executar um comando MySQL para ver se a tabela “artigos” existe, caso contrário, ela cria a tabela:</p>
<p>[php]<br>
private function criaBD() {<br>
  $sql = &lt;&lt;&lt;MySQL_QUERY<br>
    CREATE TABLE IF NOT EXISTS artigos (<br>
    id INT(11) NOT NULL AUTO_INCREMENT,<br>
    titulo VARCHAR(150),<br>
    conteudo TEXT,<br>
    data VARCHAR(100),<br>
    PRIMARY KEY (id)<br>
    )<br>
  MySQL_QUERY;<br>
  return mysql_query($sql);<br>
}<br>
[/php]</p>
<h2>Conectando ao Banco da Dados</h2>
<p>Agora que temos a função que cria tabelas no banco de dados, vamos trabalhar na função “<em>conectar()</em>” que vai permitir nosso site se conectar com o Banco de Dados:</p>
<p>[php]<br>
public function conectar() {<br>
    mysql_connect($this-&gt;host,$this-&gt;usuario,$this-&gt;senha)<br>
        or die("Não foi possível conectar. " . mysql_error());<br>
    mysql_select_db($this-&gt;bd)<br>
        or die("Não foi possível selecionar o BD. " . mysql_error());<br>
    return $this-&gt;criaBD();<br>
}<br>
[/php]</p>
<p>Observe que utilizamos duas funções do próprio PHP: <a href="http://php.net/manual/pt_BR/function.mysql-connect.php" target="_blank">mysql_connect()</a> para conectar ao servidor de banco de dados e <a href="http://php.net/manual/pt_BR/function.mysql-select-db.php" target="_blank">mysql_select_db()</a> para se certificar de operar com o banco de dados correto.&nbsp;Você pode perceber que ao utilizar as duas funções utilizamos também a função “<a href="http://php.net/manual/pt_BR/function.die.php" target="_blank">die()</a>“, para que se algo der errado, o script possa nos notificar sobre o erro de forma adequada.</p>
<p>Observe também que existe uma sequência lógica: Primeiros nos conectamos ao servidor, depois “setamos” o banco de dados que vamos trabalhar, em seguida, executamos a função “CriaBD()”, para criar as tabelas necessárias no Banco de Dados.</p>
<p><strong>Nota:</strong> É importante que você utilize o “<em>IF NOT EXISTS</em>“, já que este código será executado a cada execução.</p>
<h2>Criando um Formulário</h2>
<p>Então, se você seguiu o passo a passo até aqui, nós já teremos um banco de dados e uma tabela esperando pelos dados que você vai inserir. Então, precisamos criar um formulário aonde estes dados serão inseridos e enviados ao banco de dados.</p>
<p>[php]<br>
public function display_admin() {<br>
 return &lt;&lt;&lt;ADMIN_FORM<br>
    &lt;form action="{$_SERVER[‘PHP_SELF’]}" method="post"&gt; </p>
<p>     &lt;label for="titulo"&gt;Título:&lt;/label&gt;&lt;br /&gt;<br>
     &lt;input name="titulo" id="titulo" type="text" maxlength="150" /&gt;<br>
     &lt;div class="clear"&gt;&lt;/div&gt;</p>
<p>     &lt;label for="conteudo"&gt;Conteúdo:&lt;/label&gt;&lt;br /&gt;<br>
     &lt;textarea name="conteudo" id="conteudo"&gt;&lt;/textarea&gt;<br>
     &lt;div class="clear"&gt;&lt;/div&gt;</p>
<p>     &lt;input type="submit" value="Criar Post!" /&gt;<br>
     &lt;/form&gt;</p>
<p>     &lt;br /&gt;</p>
<p>     &lt;a href="blog.php"&gt;Voltar para Home&lt;/a&gt;<br>
    ADMIN_FORM;<br>
 }<br>
[/php]</p>
<p>Veja que é uma função simples. Estamos apenas criando um formulário HTML.</p>
<p>Um detalhe é que utilizamos uma variável pré-definida (<em>$_SERVER[‘PHP_SELF’]</em>) no atributo “<em>action</em>” do formulário, para que o formulário seja enviado para o próprio arquivo onde estará inserido. Isto é útil porque você não precisa inserir cada “URL” se for reutilizar a função.</p>
<p>Outra coisa que você vai reparar é que estamos utilizando uma sintaxe do PHP chamada Heredoc – <a href="http://us2.php.net/manual/en/language.types.string.php#language.types.string.syntax.heredoc" target="_blank">Leia sobre isso aqui</a>.</p>
<h2>Inserindo os Dados no Banco de Dados</h2>
<p>Agora já temos quase tudo que precisamos, falta gravar os dados no banco de dados que serão inseridos através do formulário. Vamos agora utilizar o método “gravar()”:</p>
<p>[php]<br>
public function gravar($p) {<br>
    if ( $_POST[‘titulo’] )<br>
        $titulo = mysql_real_escape_string($_POST[‘titulo’]);<br>
    if ( $_POST[‘conteudo’])<br>
        $conteudo = mysql_real_escape_string($_POST[‘conteudo’]);<br>
    if ( $titulo &amp;&amp; $conteudo ) {<br>
        $data = time();<br>
        $sql = "INSERT INTO artigos (titulo,conteudo,data) VALUES(‘$titulo’,’$conteudo’,’$data’)";<br>
        return mysql_query($sql);<br>
    } else {<br>
        return false;<br>
    }<br>
}<br>
[/php]</p>
<p>Neste código, estamos passando os dados do formulário utilizando a varíavel “<em>$p</em>” como argumento da função “<em>gravar()</em>“, através do <a href="http://www.w3schools.com/tags/ref_httpmethods.asp" target="_blank">método post</a>.</p>
<p>Outra coisa que estamos fazendo é testando (<a href="http://us2.php.net/manual/pt_BR/control-structures.if.php" target="_blank">estrutura de controle</a>) se os valores foram passados corretamente. Também usamos a função “<em><a href="http://us2.php.net/manual/pt_BR/function.mysql-real-escape-string.php" target="_blank">mysql_real_escape_string()</a></em>” para formatar adequadamente o valor da variável, e por medidas de segurança também.</p>
<p>Na variável “<em>$data</em>” estamos usando a função “<em><a href="http://us2.php.net/manual/pt_BR/function.time.php" target="_blank">time()</a></em>” que trabalha com o <em>timestamp do Unix</em>.</p>
<h2>Exibindo os Dados Gravados</h2>
<p>Se tudo deu certo até aqui, já temos dados gravados no nosso banco de dados. Agora, precisamos mostrar estes dados na tela. Para isto, vamos utilizar o método “<em>mostrar()</em>“. Este método pode ser um pouco mais complexo.</p>
<p>[php]<br>
public function display_public() {<br>
    $q = "SELECT * FROM artigos ORDER BY data DESC LIMIT 3";<br>
    $r = mysql_query($q);<br>
    if ( $r !== false &amp;&amp; mysql_num_rows($r) &gt; 0 ) {<br>
        while ( $a = mysql_fetch_assoc($r) ) {<br>
            $titulo = stripslashes($a[‘titulo’]);<br>
            $conteudo = stripslashes($a[‘conteudo’]);<br>
            $entry_display .= &lt;&lt;&lt;ENTRY_DISPLAY<br>
            &lt;div class="post"&gt;<br>
                &lt;h2&gt;<br>
                $titulo<br>
                &lt;/h2&gt;<br>
                &lt;p&gt;<br>
                $conteudo<br>
                &lt;/p&gt;<br>
            &lt;/div&gt;<br>
            ENTRY_DISPLAY;<br>
        }<br>
    } else {<br>
        $entry_display = &lt;&lt;&lt;ENTRY_DISPLAY<br>
        &lt;h2&gt; Este Site está em Construção &lt;/h2&gt;<br>
        &lt;p&gt;<br>
        Clique no link para adicionar novos posts!<br>
        &lt;/p&gt;<br>
        ENTRY_DISPLAY;<br>
    }<br>
    $entry_display .= &lt;&lt;&lt;ADMIN_OPTION<br>
        &lt;p class="admin_link"&gt;<br>
        &lt;a href="{$_SERVER[‘PHP_SELF’]}?admin=1"&gt;Add Novo Post&lt;/a&gt;<br>
        &lt;/p&gt;<br>
        ADMIN_OPTION;<br>
    return $entry_display;<br>
}<br>
[/php]</p>
<p>Começamos fazendo a integração do PHP com o Banco de Dados, através de uma instrução SQL. Usamos o “<em>SELECT</em>” para consultar (query) a tabela, e isto nos retornará um resultado (resource), já que também utilizamos a função “<em><a href="http://us2.php.net/manual/pt_BR/function.mysql-query.php" target="_blank">mysql_query()</a></em>“.</p>
<p>No entanto, apenas isto não vai mostrar os resultados da forma que queremos. Então, vamos trabalhar com uma matriz (<em>array</em>). A variável “<em><strong>$r</strong></em>” está recebendo os dados da tabela do banco de dados e gerando um <em>array</em>. Usamos a função “<em><a href="http://us2.php.net/manual/pt_BR/function.mysql-fetch-assoc.php" target="_blank">mysql_fetch_assoc()</a></em>” para atribuir a variável “<em><strong>$a</strong></em>” os dados associativos. Mas, esta função só nos dá uma linha de cada vez, por isso, vamos utilizar um loop “<em><a href="http://php.net/manual/en/control-structures.while.php" target="_blank">while</a></em>“.</p>
<p>Estamos verificando também se os dados foram retornados, e a função “<em><a href="http://php.net/manual/en/function.stripslashes.php" target="_blank">stripslashes()</a></em>” para tratar os dados que foram inseridos anteriormente no banco de dados. E, basicamente isto já nos retorna o que precisamos, mostrando os dados na tela.</p>
<p>É isso, você acabou de programar uma classe simples para criar um site PHP utilizando seu próprio CMS. Agora, você poderia experimentar criar outros métodos, funções e páginas para o site.</p>
<h2>Usando a Classe PHP</h2>
<p>Você criou a classe e está pronto para usá-la. Então, vamos criar um arquivo separado e chamar de “<em>blog.php</em>“. Você vai colocar este arquivo na pasta raiz do seu site.</p>
<p>Então, vamos inserir o código HTML primeiro:</p>
<p>[php]<br>
&lt;!DOCTYPE html&gt;<br>
    &lt;html lang="en"&gt;<br>
    &lt;head&gt;<br>
        &lt;title&gt;Meu CMS&lt;/title&gt;<br>
    &lt;/head&gt;<br>
    &lt;body&gt;<br>
    &lt;/body&gt;<br>
&lt;/html&gt;<br>
[/php]</p>
<p>Agora, vamos inserir um pouco de PHP dentro da tag “<em>&lt;body&gt;</em>” que vai chamar a nossa classe:</p>
<p>[php]<br>
&lt;?php</p>
<p> include_once(‘_class/CMS.php’);<br>
 $obj = new CMS();<br>
/* CHANGE THESE SETTINGS FOR YOUR OWN DATABASE */<br>
 $obj-&gt;host = ‘localhost’;<br>
 $obj-&gt;usuario = ‘usuario’;<br>
 $obj-&gt;senha = ‘senha’;<br>
 $obj-&gt;bd = ‘bancodedados’;<br>
 $obj-&gt;conectar();</p>
<p> if ($_POST)<br>
 $obj-&gt;gravar($_POST);</p>
<p> echo ( $_GET[‘admin’] == 1 ) ? $obj-&gt;display_admin() : $obj-&gt;display_public();</p>
<p> ?&gt;<br>
[/php]</p>
<p>Então, o que estamos fazendo é incluir (<em>include</em>) o arquivo da classe. Em seguida, instanciamos o nosso objeto através de “<em>new CMS()</em>“. Depois disso, vamos informar os dados de conexão do banco de dados através das nossas variáveis. Utilizaremos o método “<em>conectar()</em>” para fazer a conexão, propriamente dita.</p>
<p>Depois de conectado ao banco, vamos verificar se algum formulário está sendo enviado. Para isto, utilizamos utilizamos uma condição para a variável “<em>$_POST</em>“. Se for verdadeiro, vamos executar o método “<em>gravar()</em>“.</p>
<p>E pronto! Temos nosso próprio CMS iniciado para Criar um Site em PHP.</p>
